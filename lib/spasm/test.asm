testMain:
        PUSH    BC
        PUSH    DE
        PUSH    HL
        LD      HL, fontSBF
        CALL    writeSetFont
        LD      DE, 0                ;
        LD      HL, test_titleString ;
        CALL    writeString          ;
        LD      D, 8
        LD      B, 16
testMain_lineLoop:
        LD      A, '='
        CALL    writeCharacter
        LD      A, E
        ADD     A, 6
        LD      E, A
        DJNZ    testMain_lineLoop
        CALL    screenUpdate
        CALL    keyboardWait
        POP     HL
        POP     DE
        POP     BC
        RET

